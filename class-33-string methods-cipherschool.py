# string methods part one

name = "Ritik Kumar"

# 1.len() function
# count characters in string
print(len(name))

# 2. lower() method
# to change in small alphabet
print(name.lower())

# 3. upper method
print(name.upper())

# 4. title method
# to make first latter count
print(name.title())

# 5. count() method
# count how many same string are are there
print(name.count("i"))
